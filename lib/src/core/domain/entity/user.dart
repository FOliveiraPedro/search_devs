class User{
  String avatar;
  String name;
  String login;
  int followers;
  int following;
  String bio;
  String company;
  String location;
  String email;
  String blog;
  String twitterUsername;

  User({
    required this.avatar,
    required this.name,
    required this.login,
    required this.followers,
    required this.following,
    required this.bio,
    required this.company,
    required this.location,
    required this.email,
    required this.blog,
    required this.twitterUsername,
  });
}